    public class EmailService : IIdentityMessageService
    {
        public Task SendAsync(IdentityMessage message)
        {
            // Plug in your email service here to send an email.
            var client = Email.EmailHelper.ClassifiedSmtpClient;
            return client.SendMailAsync(ConfigurationManager.AppSettings["SupportEmailAddr"],
                message.Destination,
                message.Subject,
                message.Body);
        }

        public static string EmailConfirmationTemplate(string firstName,string lastName,string confirmationLink)
        {
            var resourceName = "Classified.Services.emailTemplates.AddEmailPrimarySubmit.html";

            var assembly = Assembly.GetExecutingAssembly();

            string emailTemplate;

            using (Stream stream = assembly.GetManifestResourceStream(resourceName))
            {
                using (StreamReader reader = new StreamReader(stream))
                {
                    emailTemplate = reader.ReadToEnd();
                }

            }
            //Replace the First Name
            emailTemplate = emailTemplate.Replace("[firstName]", firstName);
            //Replace the Last Name
            emailTemplate = emailTemplate.Replace("[lastName]", lastName);
            //Replace the Confirmation Link
            emailTemplate = emailTemplate.Replace("[confirmationLink]", confirmationLink);

            return emailTemplate;
        }
    }

